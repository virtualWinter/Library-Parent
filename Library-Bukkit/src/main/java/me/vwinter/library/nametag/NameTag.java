package me.vwinter.library.nametag;

public interface NameTag {

    /**
     * Returns the prefix.
     */
    String getPrefix();

    /**
     * Set the prefix.
     */
    void setPrefix(final String prefix);

    /**
     * Returns the suffix.
     */
    String getSuffix();

    /**
     * Set the suffix.
     */
    void setSuffix(final String suffix);

}
